package com.example.namankhanna.basketballscoreboard;

public class Players {
    String name;
    int tNum;

    public Players(String name, int tNum) {
        this.name = name;
        this.tNum = tNum;
    }

    public String getName() {
        return name;
    }

    public int gettNum() {
        return tNum;
    }
}
